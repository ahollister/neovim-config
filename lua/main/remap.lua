-- VIM REMAPS
-------------

-- Toggle NETRW
vim.keymap.set('n', '<leader>b', ":Ex<CR>")

-- Write buffer
vim.keymap.set('n', '<leader>s', ':w<CR>')

-- Toggle line numbers
vim.keymap.set("n", "<leader>n", ":set invnumber invrelativenumber<CR>")

-- Toggle whitespace
vim.keymap.set("n", "<leader>w", ":set list!<CR>")

